using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.AspNetCore.Mvc.Rendering;
using Web_Log_In_ASP.NET.Data;

namespace Web_Log_In_ASP.NET.Models
{
    public class CreateModel : PageModel
    {
        private readonly Web_Log_In_ASP.NET.Data.Web_Log_In_ASPNETContext _context;

        public CreateModel(Web_Log_In_ASP.NET.Data.Web_Log_In_ASPNETContext context)
        {
            _context = context;
        }

        public IActionResult OnGet()
        {
            return Page();
        }

        [BindProperty]
        public User User { get; set; }

        // To protect from overposting attacks, see https://aka.ms/RazorPagesCRUD
        public async Task<IActionResult> OnPostAsync()
        {
            if (!ModelState.IsValid)
            {
                return Page();
            }

            _context.User.Add(User);
            await _context.SaveChangesAsync();

            return RedirectToPage("./Index");
        }
    }
